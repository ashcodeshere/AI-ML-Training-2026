# Logical -> and, or, not
# Comparison -> =, <=, >=, == etc
# Identity -> is, is not (memory allocation of variable)
# Numerical value from -5 to 256 bits are allocated same memory space
# Membership -> in, not in (check excistence in a sequence)
# Bitwise -> Bitwise AND, Bitwise OR, Bitwise XOR, Bitwise NOT [~a=-(a+1)], Left shift, Right shift
# Left shift -> a*2^n; n=no of shifts
# Right shift -> a//2^n; n=no of shifts

